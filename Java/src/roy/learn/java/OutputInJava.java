package roy.learn.java;

public class OutputInJava {
	
		public static void main(String[] args) {
			
			//println will change the line after printting the string inside the double quote
			//print does not change line
			System.out.println("My 1st Java Code");
			System.out.print("It's a example program that explain various print function");
			System.out.println("Printing in same line");
			
			//printf will work exactly like C program
			System.out.printf("This is also a print statement\n");
			
			//we can evaluate math equation directly in java but use of " " will treate the equation as string
			System.out.println(78+2);
			System.out.println("78+2");
		}
}
